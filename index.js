function openNav(){
    document.querySelector('.sidenav').style.width = "250px";
    document.querySelector('.updte').style.marginLeft = "350px";
    document.querySelector('.person').style.marginLeft = "350px";
   
  }

  function closeNav(){
      document.querySelector('.sidenav').style.width="0"
      document.querySelector('.updte').style.marginLeft = "0";
      document.querySelector('.person').style.marginLeft = "0";
     
  }
